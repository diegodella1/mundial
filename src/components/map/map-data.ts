/**
 * Simplified world map SVG paths for ~50 countries.
 *
 * Projection (equirectangular, approximate):
 *   x = (longitude + 180) * (940 / 360) + 30   → range ~30..970
 *   y = (90 - latitude)  * (430 / 150) + 20     → range ~20..450
 *
 * ViewBox: 0 0 1000 500
 *
 * Each country is a simplified polygon (6-20 vertices) using M/L/Z commands.
 * Country ids are ISO 3166-1 alpha-2 codes.
 */

// Helper: convert (lon, lat) → (x, y) in viewBox coordinates
function p(lon: number, lat: number): [number, number] {
  const x = (lon + 180) * (940 / 360) + 30;
  const y = (90 - lat) * (430 / 150) + 20;
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
}

function poly(coords: [number, number][]): string {
  const pts = coords.map(([lon, lat]) => p(lon, lat));
  return (
    "M" +
    pts.map(([x, y]) => `${x},${y}`).join(" L") +
    " Z"
  );
}

